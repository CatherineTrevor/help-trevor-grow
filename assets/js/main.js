// Background color selection
// Check which button has been selected to change background color
let btn = document.querySelectorAll('.color_btn'); 
for (let i = 0 ; i < btn.length ; i++){
  btn[i].addEventListener('click', changeBgColor);
}

// Change background color when relevant colored button on homepage is selected
function changeBgColor() {
    btnSelect = event.target.innerHTML;
      if (btnSelect == "GREEN") {
        document.getElementById("bg_color").style.backgroundColor = "green";
    } if (btnSelect == "PURPLE") {
          document.getElementById("bg_color").style.backgroundColor = "purple";
    } if (btnSelect == "PINK") {
          document.getElementById("bg_color").style.backgroundColor = "pink";
    } if (btnSelect == "RED") {
           document.getElementById('bg_color').style.backgroundColor = "red";  
    } if (btnSelect == "BLUE") {
            document.getElementById('bg_color').style.backgroundColor = "blue";
    } if (btnSelect == "ORANGE") {
           document.getElementById('bg_color').style.backgroundColor = "orangered"; 
    }
}

// Results image to take 15 seconds to fade out from when welcome modal is closed
// https://www.w3schools.com/jquery/jquery_fade.asp - help with code. Changed to FadeTo so the page structure doesn't change
$(".close").click(function(){ // when 'close' button clicked on welcome pop-up modal
  $("#answer").fadeTo(5000, 0.01);  // fade opacity so div box doesn't disappear but image does after 15 seconds
  timer();
});  

function displayImage() {
     $("#answer_image").fadeTo('fast', 1); // change opacity back to 1 so new image displays
     timer(); // recall countdown timer function on image reload
     $("#answer_image").fadeTo(5000, 0.01); // fade opacity so div box doesn't disappear but image does after 15 seconds
};

// Countdown timer over results image to countdown from 15 as the image disappers
// https://stackoverflow.com/questions/31106189/create-a-simple-10-second-countdown - code copied and modified
function timer() {
    let timeleft = 4;
    let downloadTimer = setInterval(function(){
    if(timeleft <= 0){
        clearInterval(downloadTimer);
        document.getElementById("countdown_timer").innerHTML = "Pick the fruit to feed Trevor!";
    } else {
        document.getElementById("countdown_timer").innerHTML = timeleft;
    } 
    timeleft -= 1;
    }, 1000);
};

// Randomize and display images A, B and C for Egg level
// https://www.javatpoint.com/random-image-generator-in-javascript - copy code and modified
// shuffle the position of the three options below the answer and ensure the images are always different (so not apple, banana, apple)
function shuffleImagesEgg() {

//declare an array to store the images  
let imagesArray = [
 "/assets/images/egg_level/banana_single.jpg",
 "/assets/images/egg_level/apple_single.jpg",
 "/assets/images/egg_level/orange_single.jpg",
 "/assets/images/egg_level/lemon_single.jpg",
 "/assets/images/egg_level/pear_single.jpg",
 "/assets/images/egg_level/pineapple_single.jpg",
 "/assets/images/egg_level/strawberry_single.jpg",
]  
  
//generate a number and provide to the images to generate randomly  
let number = Math.floor(Math.random()*imagesArray.length); 
let number1 = Math.floor(Math.random()*imagesArray.length);  
let number2 = Math.floor(Math.random()*imagesArray.length);

//return the images generated by a random number  
document.getElementById("imageA").src = imagesArray[number]; // option 1 (left)
document.getElementById("imageB").src = imagesArray[number1]; // option 2 (center)
document.getElementById("imageC").src = imagesArray[number2]; /// option 3 (right)

// once displayed find image source
let answer1 = document.getElementById("imageA").src; // option 1 (left)
let answer2 = document.getElementById("imageB").src; // option 2 (center)
let answer3 = document.getElementById("imageC").src; /// option 3 (right)

// if the images next to each other are the same, reshuffle
if (answer1 === answer2) {
    shuffleImagesEgg();
} if (answer1 === answer3) {
    shuffleImagesEgg();
} if (answer2 === answer3) {
    shuffleImagesEgg();
}};
